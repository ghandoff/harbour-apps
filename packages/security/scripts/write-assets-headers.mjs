#!/usr/bin/env node
/**
 * Post-OpenNext-build step: emit a `_headers` file into `.open-next/assets/`
 * so Cloudflare's Static Assets binding serves content-hashed `_next/static/*`
 * files with `Cache-Control: public, max-age=31536000, immutable`.
 *
 * Why:
 *   CF's Workers Static Assets binding serves matched files directly without
 *   invoking worker.ts — so the @windedvertigo/security wrapper never sees
 *   those responses and can't set cache headers. The default cache-control
 *   CF emits is `public, max-age=0, must-revalidate`, which forces browsers
 *   to re-validate every chunk on every navigation. For content-hashed
 *   filenames (everything under `_next/static/`), that's wasted roundtrips.
 *
 * Usage (per-app, called after `opennextjs-cloudflare build`):
 *   node ../../packages/security/scripts/write-assets-headers.mjs /harbour/<app>
 *
 * Wire into a deploy script or run manually before `opennextjs-cloudflare deploy`.
 * Re-running is idempotent — overwrites the previous `_headers` file.
 *
 * Reference: https://developers.cloudflare.com/workers/static-assets/headers/
 */

import { writeFileSync, existsSync } from "node:fs";
import { resolve, join } from "node:path";

const basePath = process.argv[2];
if (!basePath || !basePath.startsWith("/")) {
  console.error(
    "ERROR: pass the app's basePath as arg 1 (e.g. /harbour/vertigo-vault)",
  );
  process.exit(2);
}

const cwd = process.cwd();
const assetsDir = resolve(cwd, ".open-next/assets");
if (!existsSync(assetsDir)) {
  console.error(
    `ERROR: ${assetsDir} does not exist — run 'opennextjs-cloudflare build' first`,
  );
  process.exit(3);
}

// Path patterns in `_headers` match against the URL the browser requested.
// Under basePath, content-hashed assets live at `${basePath}/_next/static/*`.
// We pin the long-cache rule to that prefix only — public/ files (favicons,
// uploaded images) stay on revalidate semantics where freshness matters.
const headersBody = `# Generated by @windedvertigo/security/scripts/write-assets-headers.mjs.
# Do not hand-edit — re-run the script after each opennextjs-cloudflare build.

${basePath}/_next/static/*
  Cache-Control: public, max-age=31536000, immutable

# Small, rarely-changing static art (boat icons / thumbnails / svgs). Not
# content-hashed, so a moderate (1 week) cache rather than immutable — avoids
# revalidating tiny assets on every navigation while letting a regen propagate.
${basePath}/images/thumbs/*
  Cache-Control: public, max-age=604800
${basePath}/icons/*
  Cache-Control: public, max-age=604800
${basePath}/harbour-preview/*
  Cache-Control: public, max-age=604800
`;

const target = join(assetsDir, "_headers");
writeFileSync(target, headersBody, "utf8");
console.log(`✓ wrote ${target}`);
console.log(`  basePath: ${basePath}`);
