/**
 * Vertigo-vault Worker entry — wraps OpenNext's emitted worker with
 * @windedvertigo/security for response-header injection, and adds a
 * scheduled() handler for the daily Notion-sync cron.
 *
 * Mirrors apps/depth-chart/worker.ts and apps/harbour/worker.ts.
 * See docs/security/cf-headers-wrapper.md for the wrapping pattern.
 *
 * GATE A.1 DECISION (Stripe DLQ store): Cloudflare KV.
 * Vault has no pre-existing Upstash/Redis dependency, so we use the
 * Worker runtime's native KV binding (STRIPE_DLQ in wrangler.jsonc) to
 * minimise the number of external services to provision at cutover.
 *
 * STAGED ONLY — not deployed. Production vault remains on Vercel until
 * the cutover plan is greenlit post-launch.
 */

