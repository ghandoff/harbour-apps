/**
 * creaseworks eval — worker entry.
 *
 * The eval flavour is built with CW_EVAL=1, which bakes basePath
 * /harbour/creaseworks-eval into assets and links, and is routed on the
 * main domain at windedvertigo.com/harbour/creaseworks-eval (zone routes
 * in wrangler.eval.jsonc). This wrapper redirects anything outside the
 * basePath — the bare basePath itself, and "/" on workers.dev — to the
 * eval home, so both entry URLs land somewhere friendly.
 *
 * Re-exports the OpenNext DO classes verbatim — wrangler drops them
 * otherwise and the deploy fails (see CLAUDE.md, raft-house exception).
 *
 * Eval-canary only: prod creaseworks keeps main = .open-next/worker.js.
 */

// @ts-ignore — generated by `npx opennextjs-cloudflare build`, gitignored
import openNextHandler, {
  // @ts-ignore — see above
  DOQueueHandler,
  // @ts-ignore — see above
  DOShardedTagCache,
  // @ts-ignore — see above
  BucketCachePurge,
} from "./.open-next/worker.js";

export { DOQueueHandler, DOShardedTagCache, BucketCachePurge };

const BASE = "/harbour/creaseworks-eval";

export default {
  fetch(request: Request, env: unknown, ctx: unknown): Promise<Response> | Response {
    const url = new URL(request.url);
    // paths outside the basePath (e.g. "/" on workers.dev) → the eval
    // entry. the bare basePath itself is served directly: the CW_EVAL
    // build rewrites "/" to the home page, so no redirect and no /eval
    // tail in the URL bar.
    if (!url.pathname.startsWith(BASE)) {
      return Response.redirect(`${url.origin}${BASE}`, 302);
    }
    return (openNextHandler as { fetch: (r: Request, e: unknown, c: unknown) => Promise<Response> }).fetch(
      request,
      env,
      ctx,
    );
  },
};
